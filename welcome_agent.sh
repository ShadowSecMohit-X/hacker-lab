1. 📄 welcome_agent.sh (The Welcome Hacker Script)
Kyun banayi: Yeh script system ka swagat karti hai aur current working context batati hai.

Isme use hui commands:

#!/bin/bash — Shebang line (Script ko batane ke liye ki ise Bash se chalana hai).

echo — Screen par text print karne ke liye.

date — System ka current time aur date dikhane ke liye.

pwd — Aap abhi kis folder mein ho (Present Working Directory) woh check karne ke liye.
